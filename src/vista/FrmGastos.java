    public void Refresh(){
        DefaultTableModel modelo = new DefaultTableModel();
        
        modelo.addColumn("Codigo");
        modelo.addColumn("Categoria");
        modelo.addColumn("Fecha");
        modelo.addColumn("Monto");
        modelo.addColumn("Descripcion");    
        modelo.addColumn("Factura");
        
        if (txtBusqueda_gastos.getText().equals("") || txtBusqueda_gastos.getText() == null) {
            gastosLista = gastosControl.listarGastos("");
        } else {
            String ssql = " where ga_descripcion like '%" + txtBusqueda_gastos.getText() + "%'";
            gastosLista = gastosControl.listarGastos(ssql);
        } 
        Object[] fila = new Object[7];
        for (int i = 0; i < gastosLista.size(); i++) {
            gastosObj = gastosLista.get(i);
            fila[0] = gastosObj.getGa_codigo();
            fila[1] = categoria[gastosObj.getGa_categoria()];
            fila[2] = gastosObj.getGa_fecha();
            fila[3] = gastosObj.getGa_monto();
            fila[4] = gastosObj.getGa_descripcion();
            fila[5] = gastosObj.getGa_factura();
            modelo.addRow(fila);
        }
        tblGastos.setModel(modelo);

        RowSorter<TableModel> ordenar = new TableRowSorter<TableModel>(modelo);
        tblGastos.setRowSorter(ordenar);

        cbxGastos.setModel(new DefaultComboBoxModel<>(categoria));
        txtFecha_gastos.setText("");
        txtMonto_gastos.setText("");
        txtDescripcion_gastos.setText("");
        checkNo.setSelected(false);
        checkSi.setSelected(false);
        txtId_gastos.setText("");
        btnGrabar.setText("Grabar");
    }

