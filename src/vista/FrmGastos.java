    private void jmModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmModificarActionPerformed
       btnGrabar.setText("    Modificar");
        Integer filaNum = tblGastos.getSelectedRow();
        if (filaNum < 0) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un registro");
        } else {
            DefaultTableModel modelo = (DefaultTableModel) tblGastos.getModel();
            Integer ga_codigo = (Integer) modelo.getValueAt(filaNum, 0);
            String whereTmp = " WHERE ga_codigo='" + ga_codigo + "'";
            gastosLista = gastosControl.listarGastos(whereTmp);
            gastosObj = gastosLista.get(0);

            txtFecha_gastos.setText(gastosObj.getGa_fecha());
            txtId_gastos.setText(gastosObj.getGa_codigo().toString());
            txtMonto_gastos.setText(String.valueOf(gastosObj.getGa_monto()));
            txtDescripcion_gastos.setText(gastosObj.getGa_descripcion());
            cbxGastos.setSelectedIndex(gastosObj.getGa_categoria());
            factura = gastosObj.getGa_factura();
            if (factura.equals("Si")) {
                checkSi.setSelected(true);
                checkNo.setSelected(false);
            } else {
                checkNo.setSelected(true);
                checkSi.setSelected(false);
            }
        }
    }//GEN-LAST:event_jmModificarActionPerformed

