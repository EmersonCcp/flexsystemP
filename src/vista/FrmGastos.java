    private void btnGrabarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGrabarMouseClicked

        
        
        try {
            factura=null;
            Integer categoriaSelecc = cbxGastos.getSelectedIndex();

            if (checkNo.isSelected()) {
                factura = "No";

            } else if (checkSi.isSelected()) {
                factura = "Si";
            }
            
           if (factura == null) {
                JOptionPane.showMessageDialog(null, "Debe seleccionar una opcion de factura");
            }
            if (checkNo.isSelected() && checkSi.isSelected()) {
                JOptionPane.showMessageDialog(null, "Debe seleccionar solo una opcion de factura");
                categoriaSelecc = 0;
            }

            if (factura != null) {
                Integer id = null;

                if (txtId_gastos.getText() != null && txtId_gastos.getText() != "") {
                    id = Integer.parseInt(txtId_gastos.getText());
                }

                String descripcion = txtDescripcion_gastos.getText();
                if ((categoriaSelecc != 0 && factura != null)&&(!txtDescripcion_gastos.getText().equals("")) ){

                    Double monto= Double.parseDouble(txtMonto_gastos.getText());
                    String fecha = txtFecha_gastos.getText();

                   gastosObj = new Gastos(id,categoriaSelecc,fecha,monto,descripcion,factura);

                    if (id == null) {
                        gastosControl.insertarGastos(gastosObj);
                        JOptionPane.showMessageDialog(null, "Gasto Agregado con Exito!");
                        this.Refresh();
                    } else {
                        gastosControl.modificarGastos(gastosObj);
                        JOptionPane.showMessageDialog(null, "Gasto Modificado con Exito!");
                        this.Refresh();
                    }

               }else {
                JOptionPane.showMessageDialog(null, "Debe completar todos los campos");
               }
            }
      } catch (NumberFormatException e) {
     JOptionPane.showMessageDialog(null, "Monto debe ser numerico"+ e );
      }
    }//GEN-LAST:event_btnGrabarMouseClicked

